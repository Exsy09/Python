cadena = "123"
if cadena.isalpha(): # Devuelve True cuando son letras
    print("Son letras")
else:
    print("No son letras")

if cadena.isdigit(): # Devuelve True si son numeros ENTEROS
    print("Son numeros")
else:
    print("No son numeros")

if cadena.isalnum(): # Devuelve True si son numeros ENTEROS
    print("Son numeros")
else:
    print("No son numeros")

if cadena.isdecimal(): # Devuelve True si son numeros ENTEROS
    print("Son numeros")
else:
    print("No son numeros")